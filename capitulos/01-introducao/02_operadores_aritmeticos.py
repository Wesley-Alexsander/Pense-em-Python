# em python os operadores aritmeticos são:
# + adição
# - subtração
# * multiplicação
# / divisão
# % resto da divisão
# ** potenciação
# // divisão inteira


# Exemplo de adição
print(10 + 5)  # 15

# Exemplo de subtração
print(10 - 5)  # 5

# Exemplo de multiplicação
print(10 * 5)  # 50

# Exemplo de divisão
print(10 / 5)  # 2.0

# Exemplo de resto da divisão
print(10 % 3)  # 1

# Exemplo de potenciação
print(10 ** 2)  # 100

# Exemplo de divisão inteira
print(10 // 3)  # 3

# Exemplo de divisão inteira com números negativos
print(-10 // 3)  # -4

# Exemplo de divisão inteira com números negativos
print(-10 // -3)  # 3


#  Em algumas outras linguagens, o ^ é usado para a
#  exponenciação, mas no Python é um operador bitwise,
#  chamado XOR. Se não tiver familiaridade com operadores
#  bitwise, o resultado o surpreenderá:

# Exemplo de operador bitwise XOR
print(6 ^ 3)  # 5

# O operador bitwise XOR (^) compara os bits de dois números e retorna 1
# se os bits forem diferentes e 0 se forem iguais.
